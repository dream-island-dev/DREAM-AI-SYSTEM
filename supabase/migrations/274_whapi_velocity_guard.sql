-- ── 4. notification_log: allow 'rate_limited' as a legal status ─────────
ALTER TABLE public.notification_log DROP CONSTRAINT IF EXISTS notification_log_status_check;
ALTER TABLE public.notification_log
  ADD CONSTRAINT notification_log_status_check
  CHECK (status IN (
    'sent', 'simulated', 'failed', 'timeout', 'blocked_by_meta',
    'processing', 'failed_missing_link', 'duplicate_blocked', 'rate_limited'
  ));

-- ── 5. pg_cron: drain whapi_outbound_jobs every 1 minute ────────────────
-- Deliberately NOT folded into whatsapp-cron's 15-min guest-automation tick —
-- a UI-triggered bulk send needs to finish in minutes, not hours, and this
-- keeps the change isolated from the existing guest pipeline's cadence/logic.
DO $$
BEGIN
  PERFORM cron.unschedule('whapi-queue-drain');
EXCEPTION WHEN OTHERS THEN
  NULL;
END $$;

SELECT cron.schedule(
  'whapi-queue-drain',
  '* * * * *',
  $$
    SELECT net.http_post(
      url     := 'https://bunohsdggxyyzruubvcd.supabase.co/functions/v1/whapi-queue-drain',
      headers := '{"Content-Type":"application/json"}'::jsonb,
      body    := '{}'::jsonb
    );
  $$
);

