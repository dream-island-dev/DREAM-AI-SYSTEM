-- Migration 274: Whapi anti-ban velocity guard
--
-- Server-enforced protection for the Suites device (Whapi 1:1 DM outbound)
-- after a real ~24h device restriction on 2026-07-23 caused by a cold bulk
-- send to ~40 waiters (WaiterPulseDispatchPanel, ~2.5s client-side gap —
-- not sufficient on its own; see docs/xos_agent_playbook.md §10). This adds:
--   1. whapi_send_ledger  — per-send audit row, used to compute recency/
--      frequency for the velocity guard (_shared/whapiVelocityGuard.ts).
--   2. whapi_outbound_jobs — durable queue for multi-recipient sends so a
--      browser tab closing mid-blast can't leave the guard's jitter/pacing
--      half-applied (_shared/whapiOutboundQueue.ts + whapi-queue-drain).
--   3. bot_config seeds for the tunable limits + post-ban cooldown state.
--   4. pg_cron schedule draining the queue every 1 minute — independent of
--      the existing 15-minute whatsapp-cron guest-automation cadence, since
--      a UI-triggered "send to 40 waiters" action needs faster draining than
--      that schedule allows.
--   5. Widens notification_log's status CHECK to accept 'rate_limited' —
--      without this, _shared/automationClaim.ts's finalizeDispatchAttempt()
--      would fail its UPDATE on a guard-blocked send, leaving the row stuck
--      at 'processing' (in_flight) instead of a clean, non-retry-gate-
--      counted rate_limited outcome. See _shared/automationRetryGate.ts —
--      buildRetryStateMap only tracks timeout/failed/blocked_by_meta/
--      processing, so rate_limited correctly falls outside cooldown/
--      exhaustion accounting once it's a legal status value.
-- Safe to re-run (idempotent).
-- ================================================================

CREATE EXTENSION IF NOT EXISTS pg_cron;
CREATE EXTENSION IF NOT EXISTS pg_net;
CREATE EXTENSION IF NOT EXISTS pgcrypto;

-- ── 1. whapi_send_ledger ─────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.whapi_send_ledger (
  id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  phone      TEXT   NOT NULL,                 -- recipient phone (bare digits) or group chat_id (…@g.us)
  risk_tier  TEXT   NOT NULL                  -- classification at send time, drives which cap applied
               CHECK (risk_tier IN ('hot', 'warm', 'cold', 'staff', 'group')),
  trigger    TEXT,
  source     TEXT,                            -- calling function, e.g. 'whatsapp-send', 'guest-club-broadcast'
  body_hash  TEXT,                             -- sha256(body) — detects identical-body blasts
  sent_at    TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_whapi_ledger_sent_at   ON public.whapi_send_ledger (sent_at DESC);
CREATE INDEX IF NOT EXISTS idx_whapi_ledger_phone     ON public.whapi_send_ledger (phone, sent_at DESC);
CREATE INDEX IF NOT EXISTS idx_whapi_ledger_body_hash ON public.whapi_send_ledger (body_hash, sent_at DESC) WHERE body_hash IS NOT NULL;

ALTER TABLE public.whapi_send_ledger ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS whapi_send_ledger_service_only ON public.whapi_send_ledger;
CREATE POLICY whapi_send_ledger_service_only ON public.whapi_send_ledger
  FOR ALL USING (auth.role() = 'service_role') WITH CHECK (auth.role() = 'service_role');

-- ── 2. whapi_outbound_jobs ───────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.whapi_outbound_jobs (
  id                UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  batch_id          UUID NOT NULL,             -- groups every row from one enqueue call for UI progress polling
  status            TEXT NOT NULL DEFAULT 'pending'
                      CHECK (status IN ('pending', 'sending', 'sent', 'failed', 'cancelled')),
  phone             TEXT NOT NULL,
  name              TEXT,                      -- recipient display name — {{שם}} substitution
  message_template  TEXT NOT NULL,             -- raw template, personalized at send time (not enqueue time)
  risk_tier         TEXT,
  trigger           TEXT,
  source            TEXT,
  scheduled_after   TIMESTAMPTZ NOT NULL DEFAULT NOW(),  -- jitter stagger applied at enqueue time
  attempts          INT NOT NULL DEFAULT 0,
  last_error        TEXT,
  wamid             TEXT,
  created_at        TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  sent_at           TIMESTAMPTZ
);

CREATE INDEX IF NOT EXISTS idx_whapi_jobs_pending
  ON public.whapi_outbound_jobs (scheduled_after)
  WHERE status = 'pending';
CREATE INDEX IF NOT EXISTS idx_whapi_jobs_batch ON public.whapi_outbound_jobs (batch_id);

ALTER TABLE public.whapi_outbound_jobs ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS whapi_jobs_read    ON public.whapi_outbound_jobs;
DROP POLICY IF EXISTS whapi_jobs_service ON public.whapi_outbound_jobs;
-- Staff UI (WaiterPulseDispatchPanel) polls batch progress directly.
CREATE POLICY whapi_jobs_read ON public.whapi_outbound_jobs
  FOR SELECT USING (auth.role() = 'authenticated');
-- Only Edge Functions (service role) enqueue/claim/finalize rows.
CREATE POLICY whapi_jobs_service ON public.whapi_outbound_jobs
  FOR ALL USING (auth.role() = 'service_role') WITH CHECK (auth.role() = 'service_role');

